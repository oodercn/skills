CREATE TABLE IF NOT EXISTS BPM_USER (
    USER_ID VARCHAR(64) PRIMARY KEY,
    USERNAME VARCHAR(128),
    DISPLAYNAME VARCHAR(255),
    EMAIL VARCHAR(255),
    DEPARTMENT VARCHAR(255),
    STATUS VARCHAR(32) DEFAULT 'ACTIVE'
);

CREATE TABLE IF NOT EXISTS BPM_ACTIVITYINST_PARTICIPANT (
    PARTICIPANT_ID VARCHAR(64) PRIMARY KEY,
    ACTIVITYINST_ID VARCHAR(64),
    USER_ID VARCHAR(64),
    ROLE_TYPE VARCHAR(32),
    PARTICIPANT_TYPE VARCHAR(32),
    ASSIGNED_TIME BIGINT,
    CLAIMED_TIME BIGINT,
    FOREIGN KEY (ACTIVITYINST_ID) REFERENCES BPM_ACTIVITYINST(ACTIVITYINST_ID),
    FOREIGN KEY (USER_ID) REFERENCES BPM_USER(USER_ID)
);

CREATE TABLE IF NOT EXISTS BPM_PROCESSDEF_PARTICIPANT (
    PARTICIPANT_ID VARCHAR(64) PRIMARY KEY,
    PROCESSDEF_ID VARCHAR(64),
    ACTIVITYDEF_ID VARCHAR(64),
    USER_ID VARCHAR(64),
    ROLE_TYPE VARCHAR(32),
    PARTICIPANT_TYPE VARCHAR(32),
    FOREIGN KEY (PROCESSDEF_ID) REFERENCES BPM_PROCESSDEF(PROCESSDEF_ID),
    FOREIGN KEY (USER_ID) REFERENCES BPM_USER(USER_ID)
);

