CREATE TABLE IF NOT EXISTS BPM_ATTRIBUTEINST (
    ATTRIBUTEINST_ID VARCHAR(64) PRIMARY KEY,
    PROCESSINST_ID VARCHAR(64),
    ACTIVITYINST_ID VARCHAR(64),
    PARENT_ID VARCHAR(64),
    ATTRIBUTENAME VARCHAR(128),
    ATTRIBUTETYPE VARCHAR(64),
    ATTRIBUTEVALUE TEXT,
    INTERPRETEDVALUE TEXT,
    FOREIGN KEY (PROCESSINST_ID) REFERENCES BPM_PROCESSINST(PROCESSINST_ID)
