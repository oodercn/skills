package net.ooder.bpm.client;

public class Perform {
    String performs;

    String readers;

    String insteadSigns;

    String esdsearchpattern;


    public Perform() {

    }

    public String getEsdsearchpattern() {
        return esdsearchpattern;
    }

    public void setEsdsearchpattern(String esdsearchpattern) {
        this.esdsearchpattern = esdsearchpattern;
    }

    public String getPerforms() {
        return performs;
    }

    public void setPerforms(String performs) {
        this.performs = performs;
    }

    public String getReaders() {
        return readers;
    }

    public void setReaders(String readers) {
        this.readers = readers;
    }

    public String getInsteadSigns() {
        return insteadSigns;
    }

    public void setInsteadSigns(String insteadSigns) {
        this.insteadSigns = insteadSigns;
    }
}
